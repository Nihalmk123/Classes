# Addition
a = 5
b = 5
sum = a + b
print("Addition of a + b:", sum)

# Subtraction
c = 5
d = 2
diff = c - d
print("Subtraction of c - d:", diff)

# Multiplication
e = 3
f = 4
product = e * f
print("Multiplication of e * f:", product)

# Division
g = 10
h = 2
division = g / h
print("Division of g / h:", division)

# Floor Division (Integer Division)
i = 10
j = 3
floor_division = i // j
print("Floor Division of i // j:", floor_division)

# Modulus (Remainder)
k = 10
l = 3
modulus = k % l
print("Modulus of k % l:", modulus)

# Exponentiation
m = 2
n = 3
exponentiation = m ** n
print("Exponentiation of m ** n:", exponentiation)
